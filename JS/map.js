function getIMGTile(name){

  var tileIMG = new Image();
  tileIMG.src = "../tilesets/" + name + ".png";
  var tileReady = false;
  tileIMG.onload = function(){tileReady = true;};

  this.img = tileIMG;
  this.ready = tileReady;

}

function MAP(name, tpr, r, c, collision, map){
	var ts = new getIMGTile(name);

	this.name = name;
	this.tpr = tpr;
	this.cols = r;
	this.rows = c;
	this.collision = collision;
	this.map = map;

	this.tileset = ts.img;
	this.ready = ts.ready;
}

function tileRange(low, high, exceptions){
	var arr = [];
	for(var i=low;i<=high;i++){
		if(exceptions.indexOf(i) == -1)
			arr.push(i);
	}
	return arr;
}

